package com.Arrays;

public class sortingmerge {
    public static void main(String[] args) {
        int arr1[][] = {{2, 4, 6}, {4, 5, 9}, {7, 8, 6}};

        for(int i=0;i<arr1.length;i++){
            for(int j=0;j<arr1.length;j++){
                System.out.print(arr1[i][j]+" ");
            }
            System.out.println();



        }
    }
}


